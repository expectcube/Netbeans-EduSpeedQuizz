    private void jButtonHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonHapusActionPerformed
int row = jTableSoal.getSelectedRow();
    if (row == -1) {
        JOptionPane.showMessageDialog(this, "Pilih soal dulu!");
        return;
    }
    int id = (int) jTableSoal.getValueAt(row, 0);
    int confirm = JOptionPane.showConfirmDialog(this, "Yakin hapus soal ini?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
    if (confirm == JOptionPane.YES_OPTION) {
        try (Connection con = Koneksi.getConnection()) {
            PreparedStatement pst = con.prepareStatement("DELETE FROM tbl_soal WHERE id = ?");
            pst.setInt(1, id);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Soal dihapus!");
            loadSoalToTable();  // Refresh
            clearFields();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Error hapus: " + ex.getMessage());
        }
    }
    }//GEN-LAST:event_jButtonHapusActionPerformed

    private void jButtonEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditActionPerformed
    int row = jTableSoal.getSelectedRow();
    if (row == -1) {
        JOptionPane.showMessageDialog(this, "Pilih soal dari tabel dulu!");
        return;
    }
    selectedSoalId = (int) jTableSoal.getValueAt(row, 0);  // Ambil ID dari kolom 0
    loadDataForEdit();  // Load data ke form
    isEdit = true;
    jButtonSimpan.setText("Update");
    }//GEN-LAST:event_jButtonEditActionPerformed
    private void resetForm() {
        jTextAreaPertanyaan.setText("");
        txtOpsiA.setText("");
        txtOpsiB.setText("");
        txtOpsiC.setText("");
        txtOpsiD.setText("");
        cbJawaban.setSelectedIndex(0);
        isEdit = false;
        selectedSoalId = -1;
        jButtonSimpan.setText("Simpan Soal");
    }
    
    private void loadDataForEdit() {
    try (Connection con = Koneksi.getConnection()) {
        PreparedStatement pst = con.prepareStatement("SELECT * FROM tbl_soal WHERE id = ?");
        pst.setInt(1, selectedSoalId);
        ResultSet rs = pst.executeQuery();
        if (rs.next()) {
            jTextAreaPertanyaan.setText(rs.getString("pertanyaan"));
            txtOpsiA.setText(rs.getString("opsi_a"));
            txtOpsiB.setText(rs.getString("opsi_b"));
            txtOpsiC.setText(rs.getString("opsi_c"));
            txtOpsiD.setText(rs.getString("opsi_d"));
            cbJawaban.setSelectedItem(rs.getString("jawaban_benar"));
        } else {
            JOptionPane.showMessageDialog(this, "Data soal tidak ditemukan!");
        }
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, "Error load data edit: " + ex.getMessage());
    }
    }
