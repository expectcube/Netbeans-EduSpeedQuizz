    private void loadQuiz() {
        try (Connection con = Koneksi.getConnection()) {
            String query = "SELECT id, nama_quiz, waktu_menit FROM tbl_quiz WHERE dosen_id = ?";
            PreparedStatement pst = con.prepareStatement(query);
            pst.setInt(1, dosenId);
            ResultSet rs = pst.executeQuery();

            DefaultTableModel model = new DefaultTableModel(new String[]{"ID", "Nama Quiz", "Waktu (menit)"}, 0);
            while (rs.next()) {
                model.addRow(new Object[]{rs.getInt("id"), rs.getString("nama_quiz"), rs.getInt("waktu_menit")});
            }
            TabelDaftarQuizDosen.setModel(model);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Error loading quiz: " + ex.getMessage());
        }
    }
