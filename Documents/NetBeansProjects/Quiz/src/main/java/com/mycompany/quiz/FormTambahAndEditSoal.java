    private void loadSoalToTable() {
    try (Connection con = Koneksi.getConnection()) {
        PreparedStatement pst = con.prepareStatement("SELECT id, pertanyaan FROM tbl_soal WHERE quiz_id = ?");
        pst.setInt(1, quizId);
        ResultSet rs = pst.executeQuery();
        DefaultTableModel model = new DefaultTableModel(new String[]{"ID", "Pertanyaan"}, 0);
        while (rs.next()) {
            model.addRow(new Object[]{rs.getInt("id"), rs.getString("pertanyaan")});
        }
        jTableSoal.setModel(model);
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, "Error load soal: " + ex.getMessage());
    }
    }

    private void loadNamaQuiz() {
    try (Connection con = Koneksi.getConnection()) {
        String query = "SELECT nama_quiz FROM tbl_quiz WHERE id = ?";
        PreparedStatement pst = con.prepareStatement(query);
        pst.setInt(1, quizId);
        ResultSet rs = pst.executeQuery();

        if (rs.next()) {
            String namaQuiz = rs.getString("nama_quiz");
            jLabelTambahSoalKeVariabelNamaSoal.setText("Edit Soal Quiz: " + namaQuiz);
        } else {
            jLabelTambahSoalKeVariabelNamaSoal.setText("Edit Soal Quiz: (Quiz tidak ditemukan)");
        }
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, "Error load nama quiz: " + ex.getMessage());
        jLabelTambahSoalKeVariabelNamaSoal.setText("Edit Soal Quiz: Error");
        ex.printStackTrace();
    }
}
