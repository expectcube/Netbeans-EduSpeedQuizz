    private void jButtonSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSimpanActionPerformed
String pertanyaan = jTextAreaPertanyaan.getText().trim();
    String opsiA = txtOpsiA.getText().trim();
    String opsiB = txtOpsiB.getText().trim();
    String opsiC = txtOpsiC.getText().trim();
    String opsiD = txtOpsiD.getText().trim();
    String jawaban = (String) cbJawaban.getSelectedItem();

    if (pertanyaan.isEmpty() || opsiA.isEmpty() || opsiB.isEmpty() || opsiC.isEmpty() || opsiD.isEmpty() || jawaban == null) {
        JOptionPane.showMessageDialog(this, "Semua field wajib diisi!");
        return;
    }

    try (Connection con = Koneksi.getConnection()) {
        PreparedStatement pst;
        if (isEdit) {
            // Mode Update
            pst = con.prepareStatement("UPDATE tbl_soal SET pertanyaan=?, opsi_a=?, opsi_b=?, opsi_c=?, opsi_d=?, jawaban_benar=? WHERE id=?");
            pst.setString(1, pertanyaan);
            pst.setString(2, opsiA);
            pst.setString(3, opsiB);
            pst.setString(4, opsiC);
            pst.setString(5, opsiD);
            pst.setString(6, jawaban);
            pst.setInt(7, selectedSoalId);
            JOptionPane.showMessageDialog(this, "Soal berhasil diupdate!");
        } else {
            // Mode Insert
            pst = con.prepareStatement("INSERT INTO tbl_soal (quiz_id, pertanyaan, opsi_a, opsi_b, opsi_c, opsi_d, jawaban_benar) VALUES (?, ?, ?, ?, ?, ?, ?)");
            pst.setInt(1, quizId);
            pst.setString(2, pertanyaan);
            pst.setString(3, opsiA);
            pst.setString(4, opsiB);
            pst.setString(5, opsiC);
            pst.setString(6, opsiD);
            pst.setString(7, jawaban);
            JOptionPane.showMessageDialog(this, "Soal berhasil ditambahkan!");
        }
        pst.executeUpdate();
        clearFields();  // Reset form
        loadSoalToTable();  // Refresh tabel
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, "Error simpan soal: " + ex.getMessage());
    }
    }//GEN-LAST:event_jButtonSimpanActionPerformed

