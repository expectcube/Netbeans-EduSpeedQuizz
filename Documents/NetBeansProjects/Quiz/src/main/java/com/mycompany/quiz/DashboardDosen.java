    private void jButtonHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonHapusActionPerformed
    int row = TabelDaftarQuizDosen.getSelectedRow();
    if (row >= 0) {
        int quizId = Integer.parseInt(TabelDaftarQuizDosen.getValueAt(row, 0).toString());
        String namaQuiz = TabelDaftarQuizDosen.getValueAt(row, 1).toString();

        int confirm = JOptionPane.showConfirmDialog(this, 
            "Yakin ingin menghapus quiz \"" + namaQuiz + "\"?\n" +
            "Semua soal dan hasil nilai terkait juga akan terhapus!", 
            "Konfirmasi Hapus", JOptionPane.YES_NO_OPTION);

        if (confirm == JOptionPane.YES_OPTION) {
            try (Connection con = Koneksi.getConnection()) {
                // Hapus hasil nilai dulu (foreign key)
                PreparedStatement pstHasil = con.prepareStatement("DELETE FROM tbl_hasil WHERE quiz_id = ?");
                pstHasil.setInt(1, quizId);
                pstHasil.executeUpdate();

                // Hapus soal
                PreparedStatement pstSoal = con.prepareStatement("DELETE FROM tbl_soal WHERE quiz_id = ?");
                pstSoal.setInt(1, quizId);
                pstSoal.executeUpdate();

                // Hapus quiz
                PreparedStatement pstQuiz = con.prepareStatement("DELETE FROM tbl_quiz WHERE id = ? AND dosen_id = ?");
                pstQuiz.setInt(1, quizId);
                pstQuiz.setInt(2, dosenId);
                int deleted = pstQuiz.executeUpdate();

                if (deleted > 0) {
                    JOptionPane.showMessageDialog(this, "Quiz berhasil dihapus!");
                    loadQuiz();  // Refresh tabel
                } else {
                    JOptionPane.showMessageDialog(this, "Gagal menghapus quiz (mungkin bukan milik Anda)");
                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, "Error hapus quiz: " + ex.getMessage());
                ex.printStackTrace();
            }
        }
    } else {
        JOptionPane.showMessageDialog(this, "Pilih quiz yang ingin dihapus!");
    }
    }//GEN-LAST:event_jButtonHapusActionPerformed

